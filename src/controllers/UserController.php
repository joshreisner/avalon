<?php

class UserController extends \BaseController {

	private static $roles = array(
		1=>'Programmer',
		2=>'Admin',
		3=>'User'
	);
	
	//show a list of users
	public function index() {
		$users = DB::table(Config::get('avalon::db_users'))->orderBy('lastname')->get();
		
		foreach ($users as &$user) {
			$user->name = $user->firstname . ' ' . $user->lastname;
			$user->role = self::$roles[$user->role];
			$user->link = URL::action('UserController@edit', $user->id);
			$user->delete = URL::action('UserController@delete', $user->id);
		}

		return View::make('avalon::users.index', array(
			'users'=>$users,
		));
	}
	
	//show the create new user form
	public function create() {
		$objects = DB::table(Config::get('avalon::db_objects'))->get();
		return View::make('avalon::users.create', array(
			'roles'=>self::$roles,
			'objects'=>$objects,
		));
	}
	
	//save new user, email them autogenerated password
	public function store() {
		$email = Input::get('email');
		$password = Str::random(12);

		$user_id = DB::table(Config::get('avalon::db_users'))->insertGetId(array(
			'firstname'=>Input::get('firstname'),
			'lastname'=>Input::get('lastname'),
			'email'=>$email,
			'password'=>Hash::make($password),
			'role'=>Input::get('role'),
			'updated_by'=>Auth::user()->id,
			'updated_at'=>new DateTime,
		));
		
		//send notification email
		Mail::send('avalon::emails.welcome', array(
			'email'=>$email,
			'password'=>$password,
			'link'=>URL::action('LoginController@getIndex'),
			), function($message) use ($email)
		{
			$message->to($email)->subject(Lang::get('avalon::messages.users_welcome_subject'));
		});		
		
		return Redirect::action('UserController@index')->with('user_id', $user_id);
	}

	//show edit screen
	public function edit($user_id) {
		$objects = DB::table(Config::get('avalon::db_objects'))->get();
		$user = DB::table(Config::get('avalon::db_users'))->where('id', $user_id)->first();
		return View::make('avalon::users.edit', array(
			'user'=>$user,
			'roles'=>self::$roles,
			'objects'=>$objects,
		));
	}

	//save edit to database
	public function update($user_id) {
		DB::table(Config::get('avalon::db_users'))->where('id', $user_id)->update(array(
			'firstname'=>Input::get('firstname'),
			'lastname'=>Input::get('lastname'),
			'email'=>Input::get('email'),
			'role'=>Input::get('role'),
			'updated_by'=>Auth::user()->id,
			'updated_at'=>new DateTime,
		));
		return Redirect::action('UserController@index')->with('user_id', $user_id);
	}

	//toggle active flag
	public function delete($user_id) {
		$deleted_at = (Input::get('active') == 1) ? null : new DateTime;
		DB::table(Config::get('avalon::db_users'))->where('id', $user_id)->update(array(
			'updated_by'=>Auth::user()->id,
			'updated_at'=>new DateTime,
			'deleted_at'=>$deleted_at,
		));
		$updated = DB::table(Config::get('avalon::db_users'))->where('id', $user_id)->pluck('updated_at');
		return Dates::relative($updated);
	}
}